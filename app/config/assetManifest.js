function toTextureBundle(basePath, ids) {
  return ids.reduce((bundle, id) => {
    bundle[id] = `${basePath}/${id}/texture.json`
    return bundle
  }, {})
}

function toSoundBundle(ids) {
  return ids.reduce((bundle, id) => {
    bundle[id] = `assets/sounds/${id}.wav`
    return bundle
  }, {})
}

